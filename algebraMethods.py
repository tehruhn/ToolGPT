
def add(a, b):
    """
    Adds Two Numbers

    Parameters
    ----------
    a : integer
        number to be added
    b : integer
        number to be added

    Returns
    -------
    integer
        sum
    """
    return a + b

def sub(a, b):
    """
    Subs Two Numbers

    Parameters
    ----------
    a : integer
        number to be subbed
    b : integer
        number to be subbed

    Returns
    -------
    int
        sub
    """
    return a - b

def mul(a, b):
    """
    Muls Two Numbers

    Parameters
    ----------
    a : integer
        number to be muled
    b : integer
        number to be muled

    Returns
    -------
    int
        mul
    """
    return a * b